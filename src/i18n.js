// Определение языка: сервер → navigator → 'en'
function detectLang() {
  return navigator.language.startsWith('ru') ? 'ru' : 'en';
}

let currentLang = detectLang();

export function getLang() {
  return currentLang;
}

// Установить язык (вызывается после загрузки профиля с сервера)
export function setLang(code) {
  if (code && LANGS[code]) currentLang = code;
}

export function t(key) {
  return (LANGS[currentLang] && LANGS[currentLang][key]) || key;
}

// Подстановка {0}, {1} в строку
export function tf(key, ...args) {
  let str = t(key);
  args.forEach((val, i) => {
    str = str.replace(`{${i}}`, val);
  });
  return str;
}
